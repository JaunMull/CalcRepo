package com.revature.logic;

public class Calculator {
    public static int add(int n1, int n2){
        int temp= n1 +n2;
        return temp;
    }

    public static int sub(int n1, int n2){
        int temp= n1 -n2;
        return temp;
    }

    public static int mult(int n1, int n2){
        int temp= n1*n2;
        return temp;
    }

    public static int div(int n1, int n2){
        int temp= n1/n2;
        return temp;
    }
}
